function openword(){
    var mod = document.getElementById("wordt");
    var times = 0;

    var startNum= setInterval(function(){
        times+=1;
        mod.innerHTML = times + "%";
        if(times==90){
            clearInterval(startNum);
        
        }
    },22.5)
}
